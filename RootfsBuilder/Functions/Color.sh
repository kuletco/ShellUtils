
# Echo Color Settings
C_CLR="\\e[0m"

C_H="\\e[1m"

C_R="${C_H}\\e[31m"
C_G="${C_H}\\e[32m"
C_B="${C_H}\\e[36m"
C_Y="${C_H}\\e[33m"

C_HR="${C_H}${C_R}"
C_HG="${C_H}${C_G}"
C_HB="${C_H}${C_B}"
C_HY="${C_H}${C_Y}"

C_OK="${C_G}OK${C_CLR}"
C_FL="${C_R}FAILED${C_CLR}"
C_WARN="${C_Y}WARNNING${C_CLR}"
C_ERROR="${C_R}ERROR${C_CLR}"
